import React,{ useRef,useEffect  } from 'react';
import { Button } from '@/components/ui/button';
import { Command, CommandEmpty, CommandGroup, CommandInput, CommandItem, CommandList } from '@/components/ui/command';
import { Popover, PopoverContent, PopoverTrigger } from '@/components/ui/popover';
import { Check, ChevronDown } from 'lucide-react';
import { cn } from '@/lib/utils';
import { Product } from '@/hooks/useProducts';

interface ProductSelectorProps {
  products: Product[];
  selectedProductId: string;
  onProductSelect: (productId: string) => void;
  open: boolean;
  onOpenChange: (open: boolean) => void;
  placeholder?: string;
  className?: string;
}

export const ProductSelector = ({
  products,
  selectedProductId,
  onProductSelect,
  open,
  onOpenChange,
  placeholder = "Select product...",
  className,
  loadMoreProducts,
  hasMore
}: ProductSelectorProps & {
  loadMoreProducts: () => void;
  hasMore: boolean;
}) => {
  console.log("hasMore:", hasMore); // üëà ADD THIS HERE
  const selectedProduct = products.find(p => p.id === selectedProductId);

  const scrollRef = useRef<HTMLDivElement | null>(null);

   useEffect(() => {
  const el = scrollRef.current;
  if (!el) return;

  const handleScroll = () => {
    console.log("scrolling...");

    const nearBottom = el.scrollTop + el.clientHeight >= el.scrollHeight - 10;
    if (nearBottom && hasMore) {
      console.log("Reached bottom, loading more...");
      loadMoreProducts();
    }
  };

  el.addEventListener("scroll", handleScroll);
  return () => el.removeEventListener("scroll", handleScroll);
}, [hasMore, loadMoreProducts]);


  return (
    // ‡¶è‡¶ñ‡¶æ‡¶®‡ßá ‡¶™‡¶∞‡¶ø‡¶¨‡¶∞‡ßç‡¶§‡¶®: PopoverContent ‡¶•‡ßá‡¶ï‡ßá max-h-60 overflow-y-auto ‡¶∏‡¶∞‡¶æ‡¶®‡ßã ‡¶π‡¶Ø‡¶º‡ßá‡¶õ‡ßá
    <PopoverContent className="w-full p-0" align="start">
  <Command>
    <CommandInput placeholder="Search products..." />
    <CommandList
      ref={scrollRef}
      // ‡¶è‡¶ñ‡¶æ‡¶®‡ßá ‡¶™‡¶∞‡¶ø‡¶¨‡¶∞‡ßç‡¶§‡¶®: CommandList-‡¶è max-h-60 overflow-y-auto ‡¶Ø‡ßã‡¶ó ‡¶ï‡¶∞‡¶æ ‡¶π‡¶Ø‡¶º‡ßá‡¶õ‡ßá
      className="max-h-60 overflow-y-auto"
    >
      <CommandEmpty>No product found.</CommandEmpty>
      <CommandGroup>
        {products.map((product) => (
          <CommandItem
            key={product.id}
            value={product.name}
            onSelect={() => {
              onProductSelect(product.id);
              onOpenChange(false);
            }}
          >
            {/* ... */}
          </CommandItem>
        ))}
        {hasMore && (
          <div className="text-center text-sm p-2 text-muted-foreground">
            Loading more...
          </div>
        )}
      </CommandGroup>
    </CommandList>
  </Command>
</PopoverContent>

  );
};